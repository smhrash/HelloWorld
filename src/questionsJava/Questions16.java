package questionsJava;

public class Questions16 {

    // Write a Java Program to generate a Multiplication table
    // 12 x 1 = 12
    // 12 x 2 = 24
    // 12 x 3 = 36
    // 12 x 4 = 48

    public static void main(String[] args) {

        int num = 12;

        for (int i = 1; i <=10; i++){

            System.out.println(num+" x "+i+" = "+num*i);
        }

    }
}
